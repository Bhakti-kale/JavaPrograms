package basic_programs;

public class MathClass_12 {
	public static void main(String[] args)
	{

	System.out.println(Math.addExact(100, 200));
	System.out.println(Math.addExact(3234456669l, 16788954));/////used l in 1st arg value
	System.out.println(Math.subtractExact(200, 100));
	System.out.println(Math.subtractExact(345677888, 67885433333l));//used l
	System.out.println(Math.multiplyExact(100, 10));
	System.out.println(Math.multiplyExact(34566777, 765448l));//used l
	System.out.println(Math.max(2.3, 6.3));
	System.out.println(Math.min(5, 6));
	System.out.println(Math.abs(-800));
	System.out.println(Math.random());
	System.out.println(Math.PI);



	}

}
